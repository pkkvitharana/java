

package com.mycompany.q3;


public class Q3 {

    public static void main(String[] args) {
        //for loop
        for(int i=0;i<5;i++)
        {
         System.out.println("Executing Loop "+i);   
        }
        //while loop
        int counter=0;
        while (counter<5)
        {
            System.out.println("Executing Loop "+counter);
            counter++;
        }
        
    }
}
